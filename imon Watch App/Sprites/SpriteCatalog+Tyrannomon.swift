        let idle2 = SpriteFrame(rows: [
            0x0000, 0x0000,
            0x07C0, 0x0FE0, 0x0DA0, 0x0FE0,
            0x07C0, 0x0FE0, 0x1FF0, 0x5FF0,
            0x1FF0, 0x0FE0, 0x0FE0,
            0x1220, //  ...#..#..#......  alt legs
            0x1230, //  ...#..#..##.....
            0x0000
        ])

        // Fire Breath (Blaze Blast) â€” head forward, mouth wide, flame cluster
