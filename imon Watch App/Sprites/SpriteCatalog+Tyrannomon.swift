    ) -> [SpriteFrame] {
        let idle1 = SpriteFrame(rows: [
            0x0000, //  ................
            0x0000, //  ................
            0x07C0, //  .....#####......  head
            0x0FE0, //  ....#######.....
            0x0DA0, //  ....##.##.#.....  eye
            0x0FE0, //  ....#######.....
            0x07C0, //  .....#####......  neck
            0x0FE0, //  ....#######.....  body
